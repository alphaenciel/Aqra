public class Category1 {
    int id;
    String name;

    public Category1(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Category1() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
